    private void butonJugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butonJugarActionPerformed
        // TODO add your handling code here:
        try {
            if (grupoJuego.getSelection().equals(radioJEA.getModel())) {
                Tablero _tablero = new TableroNormal(new EstrategiaNormal(), new int[7][6]);
                Main._juego = new JuegoNormal(_tablero);
                Main._juego.iniciarJuego(new EstadoJugar());
                radioGravedad.setEnabled(false);
                crearVistas(_tablero);
            }
            else if (grupoJuego.getSelection().equals(radioGravedad.getModel())) {
                Tablero _tablero = new TableroNormal(new EstrategiaGravedad(), new int[7][6]);
                Main._juego = new JuegoGravedad(_tablero);
                Main._juego.iniciarJuego(new EstadoJugar());
                radioJEA.setEnabled(false);
                radioComplica.setEnabled(false);
                crearVistas(_tablero);
            }
            else if (grupoJuego.getSelection().equals(radioComplica.getModel())) {
                Tablero _tablero = new TableroNormal(new EstrategiaComplica(), new int[6][4]);
                Main._juego = new JuegoComplica(_tablero);
                Main._juego.iniciarJuego(new EstadoJugar());
                radioGravedad.setEnabled(false);
                crearVistas(_tablero);
            }
            spinnerVistas.setEnabled(false);
            butonJugar.setEnabled(false);
            butonTerminar.setEnabled(true);
            Main._frame.pack();
        }
        catch (NullPointerException ex) {
            JOptionPane.showConfirmDialog(this, "Selecciona un juego", "OJO", JOptionPane.CLOSED_OPTION);
        }
    }//GEN-LAST:event_butonJugarActionPerformed

