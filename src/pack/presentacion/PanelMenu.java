    private void butonTerminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butonTerminarActionPerformed
        // TODO add your handling code here:
        spinnerVistas.setEnabled(true);
        radioJEA.setEnabled(true);
        radioGravedad.setEnabled(true);
        radioComplica.setEnabled(true);
        butonJugar.setEnabled(true);
        butonTerminar.setEnabled(false);
        Main._frame.getContentPane().remove(1);
        Main._juego = null;
        Main._frame.pack();
        for (JFrame jFrame : vistasAux)
            jFrame.dispose();
        vistasAux.removeAll(vistasAux);
    }//GEN-LAST:event_butonTerminarActionPerformed

    private void butonVistasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butonVistasActionPerformed
        // TODO add your handling code here:
        for (JFrame jFrame : vistasAux) {
            if (!jFrame.isVisible())
                jFrame.setVisible(true);
        }
    }//GEN-LAST:event_butonVistasActionPerformed

    private void crearVistas(Tablero _tablero) {
        for (int i = 0; i < Integer.parseInt(spinnerVistas.getValue().toString()); i++) {
            TableroGrafico _tableroGrafico = new Tablero1(_tablero);
            _tableroGrafico.requestFocus();
            
            if (i == 0) {
                _tableroGrafico.setPreferredSize(new Dimension(_tablero.getGrilla()[0].length * 60 + 2, _tablero.getGrilla().length * 60 + 32));
                Main._frame.getContentPane().add(_tableroGrafico, BorderLayout.CENTER);
            }
            else {
                _tableroGrafico.setPreferredSize(new Dimension(_tablero.getGrilla()[0].length * 60 + 16, _tablero.getGrilla().length * 60 + 34));
                JFrame frame = new JFrame("Vista " + i);
                frame.add(_tableroGrafico);
                frame.pack();
                frame.setVisible(true);
                vistasAux.add(frame);
            }
            Main._juego.agregarObservador(_tableroGrafico);
        }
    }
    
